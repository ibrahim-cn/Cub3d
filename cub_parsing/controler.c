#include "../cub3d.h"

int	check_tab(char *line)
{
	int	i;

	i = 0;
	while (line[i])
	{
		if (line[i] == '\t')
			return (1);
		i++;
	}
	return (0);
}

int	ft_empty(char c)
{
	if (c == '\n' || c == ' ')
		return (1);
	return (0);
}